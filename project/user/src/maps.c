uint8 map_count(void)
{
    return (uint8)(sizeof(offline_maps) / sizeof(offline_maps[0]));
}

const map_source_struct *map_get(uint8 index)
{
    uint8 count = map_count();

    if(index >= count)
    {
        index = 0;
    }
    return &offline_maps[index];
}
